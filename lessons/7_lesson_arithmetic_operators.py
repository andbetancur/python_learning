# arithmetic operators
num1 = 3
num2 = 2
# sum
sum = num1 + num2
print(f"The sum is {sum}") # mixing str with int

# substract
substrack = num1 - num2
print(f"The substrack is {substrack}")

# multiplication
mult = num1 * num2
print(f"the multiplication is {mult}")

# division
div = num1 / num2
print(f"The division is {div}")

# floor
floor = num1 // num2
print(f"The floor is {floor}")

# modulus
module = num1 % num2
print(f"The module is {module}")

# exponential
expo = num2 ** num1
print(f"The exponentiation is: {expo}")
