# comparison operators

# equal to ==
number = 5 == 5
print(f"Is it equal? {number}")

# not equal !=
not_eq = 5 != 3
print(f"Is it not equal? {not_eq}")

# grather than or less than
grat = 6 > 3
print(f"Is it grather? {grat}")

# grather or equal than less or equal than
